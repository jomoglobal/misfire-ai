# ---------------------------------------------------------------------------
# _read_vbo parsing tests
# ---------------------------------------------------------------------------

class TestReadVbo:
    def test_returns_correct_column_names(self, minimal_vbo_file):
        cols, rows = _read_vbo(minimal_vbo_file)
        assert cols == MINIMAL_VBO_COLS

    def test_returns_correct_row_count(self, minimal_vbo_file):
        cols, rows = _read_vbo(minimal_vbo_file)
        assert len(rows) == 3

    def test_rpm_values_are_numeric_strings(self, minimal_vbo_file):
        cols, rows = _read_vbo(minimal_vbo_file)
        rpm_vals = [float(r["rpm-obd"]) for r in rows]
        assert rpm_vals == [1200.0, 1500.0, 2000.0]

    def test_timing_advance_absent_when_data_field_missing(self, minimal_vbo_file):
        # timing_advance-obd is the last declared column but data rows have one
        # fewer field — it is naturally absent from parsed rows (zip stops early).
        cols, rows = _read_vbo(minimal_vbo_file)
        assert "timing_advance-obd" not in rows[0]

    def test_malformed_short_trailing_row_is_skipped(self, tmp_path):
        # A row with far too few fields (< half expected) should be skipped
        vbo = MINIMAL_VBO + "010\n"  # single field, clearly malformed
        p = tmp_path / "short_row.vbo"
        p.write_text(vbo, encoding="utf-8")
        cols, rows = _read_vbo(str(p))
        # Short row skipped — 3 good rows remain
        assert len(rows) == 3

    def test_empty_data_section_returns_no_rows(self, tmp_path):
        vbo = textwrap.dedent("""\
            [header]
            rpm-obd

            [column names]
            rpm-obd

            [data]
        """)
        p = tmp_path / "empty_data.vbo"
        p.write_text(vbo, encoding="utf-8")
        cols, rows = _read_vbo(str(p))
        assert rows == []


