# ---------------------------------------------------------------------------
# detect_source tests
# ---------------------------------------------------------------------------

class TestDetectSource:
    def test_detects_dragy_from_obd_suffix_columns(self):
        assert detect_source(MINIMAL_VBO_COLS) == "dragy"

    def test_detects_dragy_with_only_one_obd_column(self):
        assert detect_source(["sats", "time", "rpm-obd"]) == "dragy"

    def test_dragy_not_misclassified_as_mhd(self):
        # MHD has (*F), WGDC, STFT 1 — dragy has none of those
        assert detect_source(MINIMAL_VBO_COLS) != "mhd"

    def test_dragy_not_misclassified_as_car_scanner(self):
        assert detect_source(MINIMAL_VBO_COLS) != "car_scanner"

    def test_dragy_not_misclassified_as_carobd(self):
        assert detect_source(MINIMAL_VBO_COLS) != "carobd"

    def test_dragy_not_misclassified_as_cephasax(self):
        assert detect_source(MINIMAL_VBO_COLS) != "cephasax"

    def test_dragy_not_misclassified_as_isay_gerard(self):
        assert detect_source(MINIMAL_VBO_COLS) != "isay_gerard"

    def test_mhd_not_misclassified_as_dragy(self):
        mhd_cols = ["RPM", "Boost (PSI)", "STFT 1 (%)", "Coolant (*F)"]
        assert detect_source(mhd_cols) == "mhd"

    def test_car_scanner_not_misclassified_as_dragy(self):
        cs_cols = ["Engine RPM (rpm)", "Engine coolant temperature (℉)"]
        assert detect_source(cs_cols) == "car_scanner"


