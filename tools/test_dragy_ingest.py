class TestSourceColumnMap:
    def test_obd_columns_map_to_canonical_names(self):
        expected = {
            "rpm-obd":           "RPM",
            "speed-obd":         "VSS",
            "coolant_temp-obd":  "ECT",
            "intake_temp-obd":   "IAT",
            "manifold_flow-obd": "MAF",
            "throttle_pos-obd":  "THROTTLE",
            "engine_load-obd":   "LOAD",
            "timing_advance-obd":"TIMING_ADV",
        }
        for raw_col, expected_canon in expected.items():
            result = SOURCE_COLUMN_MAP.get(("dragy", raw_col))
            assert result == expected_canon, f"({raw_col!r} → {result!r}, expected {expected_canon!r})"

    def test_gps_motion_columns_are_explicitly_none(self):
        skip_cols = ["sats", "time", "lat", "long", "velocity", "heading", "height", "acceleration-obd"]
        for col in skip_cols:
            result = SOURCE_COLUMN_MAP.get(("dragy", col), "NOT_IN_MAP")
            assert result is None, f"{col!r} should be None (explicit skip), got {result!r}"

    def test_obd_columns_not_silently_absent_from_map(self):
        obd_cols = [
            "rpm-obd", "speed-obd", "coolant_temp-obd", "intake_temp-obd",
            "manifold_flow-obd", "throttle_pos-obd", "engine_load-obd", "timing_advance-obd",
        ]
        for col in obd_cols:
            assert ("dragy", col) in SOURCE_COLUMN_MAP, f"Missing dragy mapping for {col!r}"


# ---------------------------------------------------------------------------
# Full ingest_file integration test
# ---------------------------------------------------------------------------

class TestIngestFileSample:
    """Uses the real 14,952-row sample fixture."""

    @pytest.fixture(autouse=True)
    def skip_if_missing(self):
        if not os.path.exists(SAMPLE_VBO):
            pytest.skip(f"Sample fixture not found: {SAMPLE_VBO}")

    def test_source_is_dragy(self):
        result = json.loads(ingest_file(SAMPLE_VBO))
        assert result.get("source") == "dragy"

    def test_row_count_default_cap(self):
        result = json.loads(ingest_file(SAMPLE_VBO))
        # Default max_rows=500 caps the reported row_count
        assert result["row_count"] == 500

    def test_all_obd_pids_present(self):
        result = json.loads(ingest_file(SAMPLE_VBO))
        pids = result.get("pids", {})
        # TIMING_ADV is declared in [column names] but absent from this capture's
        # data rows (column 15, data only has 15 fields 0-14). The other 7 must be present.
        expected_pids = {"RPM", "VSS", "ECT", "IAT", "MAF", "THROTTLE", "LOAD"}
        missing = expected_pids - set(pids.keys())
        assert not missing, f"Missing PIDs: {missing}"

    def test_no_gps_motion_pids_in_output(self):
        result = json.loads(ingest_file(SAMPLE_VBO))
        pids = result.get("pids", {})
        gps_names = {"sats", "time", "lat", "long", "velocity", "heading", "height", "acceleration-obd"}
        leaked = gps_names & set(pids.keys())
        assert not leaked, f"GPS/motion cols leaked into pids: {leaked}"

    def test_no_recognizable_obd2_columns_error_absent(self):
        result = json.loads(ingest_file(SAMPLE_VBO))
        assert "error" not in result

    def test_rpm_values_in_plausible_range(self):
        result = json.loads(ingest_file(SAMPLE_VBO, max_rows=15000))
        rpm = result["pids"]["RPM"]
        assert 500 <= rpm["mean"] <= 8000

    def test_coolant_temp_in_plausible_range(self):
        # Dragy reports coolant in °C already — no conversion needed
        result = json.loads(ingest_file(SAMPLE_VBO, max_rows=15000))
        ect = result["pids"]["ECT"]
        assert 60 <= ect["mean"] <= 120

    def test_full_sample_row_count_with_high_max(self):
        result = json.loads(ingest_file(SAMPLE_VBO, max_rows=20000))
        # 14,952 good rows in sample
        assert result["row_count"] == 14952


